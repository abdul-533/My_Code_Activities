package Gun13_While_Loop_and_Do_While;

public class _02_WhileLoop {
    public static void main(String[] args) {
        // Print hello to the screen 5 times
        int counter = 0;
        while (counter < 5) // write the condition
        {
            System.out.println(counter + ". Hello");

            counter = counter + 1; //(counter++)
        }

        System.out.println("Program is ower");


    }
}
