package Gun15_Nested_Loop;

public class _08_JavaNestedLoop {
    public static void main(String[] args) {

        //Question:  Print all odd numbers from 100 to 0.
        System.out.println("Start With ");
        for (int i = 100; i >=0 ; i--) {
            System.out.println(i+": ");
            if (i==0) {
                System.out.println(i + "."+"The END");
                break;
            }


        }

    }
}
