package Gun15_Nested_Loop;

public class _01_JavaNestedLoop {
    public static void main(String[] args) {
        // Question: Print 5 stars on the screen using a single star
        // Example:
        // *****
        // *****
        // *****
        // *****
        // *****

        for (int j = 0; j < 5; j++) {
            System.out.println();
            for (int i = 0; i < 5; i++)
                System.out.print("*");
        }


    }
}
